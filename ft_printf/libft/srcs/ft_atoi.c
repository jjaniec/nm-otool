int		ft_atoi(const char *str)
{
	long long	rslt;
	int			i;
	int			j;

	i = 0;
	j = 0;
	rslt = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\r' || \
			str[i] == '\v' || str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		j = i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		rslt *= 10;
		rslt += str[i] - '0';
		i++;
	}
	return ((str[j] == '-') ? (-rslt) : (rslt));
}
