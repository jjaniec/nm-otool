#include <ft_nm.h>

uint32_t		swap_32bit(uint32_t x)
{
	return ((((x) & 0xff000000) >> 24) | \
		(((x) & 0xff0000) >> 8) | \
		(((x) & 0xff00) << 8) | \
		(((x) & 0xff) << 24));
}
